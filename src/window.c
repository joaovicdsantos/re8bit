#define WINDOW_WIDTH 1920
#define WINDOW_HEIGHT 960
#define BIT_SIZE 32
#define RECTANGLE_SIZE 30

#define MAX_COLORS 5
#define COLORS sfWhite, sfGreen, sfRed, sfBlue, sfYellow

#define MIN_TICK 10
#define MAX_TICK 1500
#define TICK_STEP 10

#define FONT_SIZE 20

const uint8_t font_mem[10008];

sfRenderWindow *new_window()
{
    return sfRenderWindow_create((sfVideoMode){WINDOW_WIDTH, WINDOW_HEIGHT, BIT_SIZE}, "Re8Bit", sfDefaultStyle, NULL);
}

sfText *new_text()
{
    sfFont *font = sfFont_createFromMemory(font_mem, sizeof(font_mem));
    sfText *text = sfText_create();
    sfText_setFont(text, font);
    sfText_setFillColor(text, sfWhite);
    sfText_setCharacterSize(text, FONT_SIZE);
    return text;
}

void draw(sfRenderWindow *window, uint8_t *display, sfText *info_text, double fps, uint8_t color_index, uint16_t tick)
{
    sfColor colors[] = {COLORS};
    sfRectangleShape *rectangle = sfRectangleShape_create();
    sfRectangleShape_setSize(rectangle, (sfVector2f){RECTANGLE_SIZE, RECTANGLE_SIZE});
    sfRectangleShape_setFillColor(rectangle, colors[color_index]);

    uint16_t x = 0, y = 0;
    for (uint16_t i = 0; i < DISPLAY_SIZE; i++)
    {
        if (display[i] == 1)
        {
            sfRectangleShape_setPosition(rectangle, (sfVector2f){x, y});
            sfRenderWindow_drawRectangleShape(window, rectangle, NULL);
        }

        x += RECTANGLE_SIZE;
        if (x >= WINDOW_WIDTH)
        {
            x = 0;
            y += RECTANGLE_SIZE;
        }
    }
    sfRectangleShape_destroy(rectangle);

    char info_str[30];
    sprintf(info_str, "FPS: %.2f | Tick: %d", fps, tick);
    sfText_setString(info_text, info_str);
    sfText_setPosition(info_text, (sfVector2f){15, WINDOW_HEIGHT - 35});
    sfRenderWindow_drawText(window, info_text, NULL);
}

void window_event_mapping(sfRenderWindow *window, sfEvent *event, Chip8 *chip8, uint8_t *color_index, uint16_t *tick)
{
    switch (event->type)
    {
    case sfEvtClosed:
        sfRenderWindow_close(window);
        break;
    case sfEvtKeyPressed:
        switch (event->key.code)
        {
        case sfKeyNum1:
            chip8->keys[0x1] = true;
            break;
        case sfKeyNum2:
            chip8->keys[0x2] = true;
            break;
        case sfKeyNum3:
            chip8->keys[0x3] = true;
            break;
        case sfKeyNum4:
            chip8->keys[0xC] = true;
            break;
        case sfKeyQ:
            chip8->keys[0x4] = true;
            break;
        case sfKeyW:
            chip8->keys[0x5] = true;
            break;
        case sfKeyE:
            chip8->keys[0x6] = true;
            break;
        case sfKeyR:
            chip8->keys[0xD] = true;
            break;
        case sfKeyA:
            chip8->keys[0x7] = true;
            break;
        case sfKeyS:
            chip8->keys[0x8] = true;
            break;
        case sfKeyD:
            chip8->keys[0x9] = true;
            break;
        case sfKeyF:
            chip8->keys[0xE] = true;
            break;
        case sfKeyZ:
            chip8->keys[0xA] = true;
            break;
        case sfKeyX:
            chip8->keys[0x0] = true;
            break;
        case sfKeyC:
            chip8->keys[0xB] = true;
            break;
        case sfKeyV:
            chip8->keys[0xF] = true;
            break;
        case sfKeyP:
            chip8->paused = !chip8->paused;
            break;
        case sfKeyO:
            *color_index = (*color_index >= (MAX_COLORS - 1)) ? 0 : *color_index + 1;
            break;
        case sfKeyM:
            chip8->muted = !chip8->muted;
            break;
        case sfKeyUp:
            *tick = (*tick + TICK_STEP) > MAX_TICK ? MAX_TICK : *tick + TICK_STEP;
            break;
        case sfKeyDown:
            *tick = (*tick - TICK_STEP) < MIN_TICK ? MIN_TICK : *tick - TICK_STEP;
            break;
        default:
            break;
        }
        break;
    case sfEvtKeyReleased:
        switch (event->key.code)
        {
        case sfKeyNum1:
            chip8->keys[0x1] = false;
            break;
        case sfKeyNum2:
            chip8->keys[0x2] = false;
            break;
        case sfKeyNum3:
            chip8->keys[0x3] = false;
            break;
        case sfKeyNum4:
            chip8->keys[0xC] = false;
            break;
        case sfKeyQ:
            chip8->keys[0x4] = false;
            break;
        case sfKeyW:
            chip8->keys[0x5] = false;
            break;
        case sfKeyE:
            chip8->keys[0x6] = false;
            break;
        case sfKeyR:
            chip8->keys[0xD] = false;
            break;
        case sfKeyA:
            chip8->keys[0x7] = false;
            break;
        case sfKeyS:
            chip8->keys[0x8] = false;
            break;
        case sfKeyD:
            chip8->keys[0x9] = false;
            break;
        case sfKeyF:
            chip8->keys[0xE] = false;
            break;
        case sfKeyZ:
            chip8->keys[0xA] = false;
            break;
        case sfKeyX:
            chip8->keys[0x0] = false;
            break;
        case sfKeyC:
            chip8->keys[0xB] = false;
            break;
        case sfKeyV:
            chip8->keys[0xF] = false;
            break;
        default:
            break;
        }
        break;
    default:
        break;
    }
}

